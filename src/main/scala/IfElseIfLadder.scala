/* Scala program to illustrate the if-else-if ladder */
class IfElseIfLadder {

  def ifElseIfLadderExample(): Unit = {
    var value: Int = 50
    if (value == 20) {
      println("- Value is 20")
    }
    else if (value == 25) {
      println("- Value is 25")
    }
    else if (value == 40) {
      println("- Value is 40")
    }
    else {
      println("- No Match Found")
    }
  }

}
